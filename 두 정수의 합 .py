# 사용자에게 두 수를 입력받기
# 첫 번째 숫자를 입력받는다
num1 = int(input("첫 번째 숫자를 입력하세요: ")) # 입력받은 값을 정수로 변환
# 두 번째 숫자를 입력받는다
num2 = int(input("두 번째 숫자를 입력하세요: ")) # 입력받은 값을 정수로 변환
# 두 수를 더한다
total = num1 + num2 # sum은 내장 함수이기도 하니 total을 사용하는 것도 괜찮음
# 결과를 출력한다
print(f"두 수의 합은 {total} 입니다.")